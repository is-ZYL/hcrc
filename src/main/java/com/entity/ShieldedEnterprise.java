package com.entity;

public class ShieldedEnterprise {
	/**
	 * 个人用户屏蔽企业
	 */
	private int se_id;
	
	private Users user;
	
	private Enterprise enterprise;
	
	private String se_shieldedTime;

	public int getSe_id() {
		return se_id;
	}

	public void setSe_id(int se_id) {
		this.se_id = se_id;
	}

	public Users getUser() {
		return user;
	}

	public void setUser(Users user) {
		this.user = user;
	}

	public Enterprise getEnterprise() {
		return enterprise;
	}

	public void setEnterprise(Enterprise enterprise) {
		this.enterprise = enterprise;
	}

	public String getSe_shieldedTime() {
		return se_shieldedTime;
	}

	public void setSe_shieldedTime(String se_shieldedTime) {
		this.se_shieldedTime = se_shieldedTime;
	}

	@Override
	public String toString() {
		return "ShieldedEnterprise [se_id=" + se_id + ", user=" + user + ", enterprise=" + enterprise
				+ ", se_shieldedTime=" + se_shieldedTime + "]";
	}
	
	
}
