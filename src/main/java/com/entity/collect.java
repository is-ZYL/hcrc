package com.entity;

public class collect {
	/**
	 * 职位收藏
	 */
	private int c_id;
	
	private Users users;
	
	private Position position;
	
	private String c_collectionTime;

	public int getC_id() {
		return c_id;
	}

	public void setC_id(int c_id) {
		this.c_id = c_id;
	}

	public Users getUsers() {
		return users;
	}

	public void setUsers(Users users) {
		this.users = users;
	}

	public Position getPosition() {
		return position;
	}

	public void setPosition(Position position) {
		this.position = position;
	}

	public String getC_collectionTime() {
		return c_collectionTime;
	}

	public void setC_collectionTime(String c_collectionTime) {
		this.c_collectionTime = c_collectionTime;
	}

	@Override
	public String toString() {
		return "Collection [c_id=" + c_id + ", users=" + users + ", position=" + position + ", c_collectionTime="
				+ c_collectionTime + "]";
	}
	
	
}
