package com.entity;

public class HeadhuntingCandidate {
	/**
	 * 猎头候选人
	 */
	private int hc_id;
	
	private LaborPersonnel labor_personnel;
	
	private String hc_sendTime;
	
	private Headhunting headhunting;

	public int getHc_id() {
		return hc_id;
	}

	public void setHc_id(int hc_id) {
		this.hc_id = hc_id;
	}

	public LaborPersonnel getLabor_personnel() {
		return labor_personnel;
	}

	public void setLabor_personnel(LaborPersonnel labor_personnel) {
		this.labor_personnel = labor_personnel;
	}

	public String getHc_sendTime() {
		return hc_sendTime;
	}

	public void setHc_sendTime(String hc_sendTime) {
		this.hc_sendTime = hc_sendTime;
	}

	public Headhunting getHeadhunting() {
		return headhunting;
	}

	public void setHeadhunting(Headhunting headhunting) {
		this.headhunting = headhunting;
	}

	@Override
	public String toString() {
		return "HeadhuntingCandidate [hc_id=" + hc_id + ", labor_personnel=" + labor_personnel + ", hc_sendTime="
				+ hc_sendTime + ", headhunting=" + headhunting + "]";
	}
	
}
